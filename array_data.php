function destinationList( $selecteddestination ) {
    global $journey;
    foreach($journey as $key => $item){
        $selected = '';
        if( key_exists( $selecteddestination, $journey ) && $selecteddestination == $key ) {
            $selected = 'selected';
        }
        printf( '<option value="%s" %s>%s</option>', strtolower( $key ), $selected, ucwords( $key ) );
    }
}

function operatorList( $selecteddestination, $selectedOperator ) {
    global $journey;
    foreach( $journey[$selecteddestination] as $key => $item ) {
        $selected = '';
        if( key_exists( $selectedOperator, $journey[$selecteddestination] ) && $selectedOperator == $key ) {
            $selected = 'selected';
        }
        printf( '<option value="%s" %s>%s</option>', strtolower( $key ), $selected, ucwords( $key ) );
    }
}

function classList( $selecteddestination, $selectedOperator, $selectedClass ) {
